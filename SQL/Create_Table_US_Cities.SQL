CREATE TABLE US_Cities (
	City_ID int NOT NULL AUTO_INCREMENT,
	State VARCHAR(255),
	City VARCHAR(255),
	Latitude DECIMAL(4,10),
	Longitude DECIMAL(4,10),
	Year_2010 INT,
	Year_2011 INT,
	Year_2012 INT,
	Year_2013 INT,
	Year_2014 INT,
	Year_2015 INT,
	Year_2016 INT,
	Year_2017 INT,
	Year_2018 INT,
	Year_2019 INT,
	PRIMARY KEY (City_ID)
);